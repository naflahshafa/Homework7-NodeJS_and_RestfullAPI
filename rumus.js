// Local Module
exports.luasPersegi = function(number1) {
    return number1 * number1
}

exports.kelilingPersegi = function(number1) {
    return number1 * 4
}

exports.luasPersegiPanjang = function(number1, number2) {
    return number1 * number2
}

exports.kelilingPersegiPanjang = function(number1, number2) {
    return (2 * number1) + (2 * number2)
}